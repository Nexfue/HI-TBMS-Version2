export function getFilteredHotelLocations(search) {
  if (!search) return HOTEL_LOCATIONS.slice(0, 20);

  const q = search.toLowerCase();

  return HOTEL_LOCATIONS.filter(city =>
    city.toLowerCase().includes(q)
  ).slice(0, 15);
}